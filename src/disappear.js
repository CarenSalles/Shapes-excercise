let circle = document.getElementById('container-shapes__circle');
let square = document.getElementById('container-shapes__square');
let triangle = document.getElementById('container-shapes__triangle');

function disappearCircle() {

 if(circle.style.display = 'block'){

  circle.style.display = 'none';
 }

}

function disappearSquare() {

  if (square.style.display = 'block') {
    
   square.style.display = 'none';
 }
    
}

function disappearTriangle() {

  if (triangle.style.display = 'block') {
        
  triangle.style.display = 'none';        
 }
    
}